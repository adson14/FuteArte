    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jtFuncionarios.getSelectedRow() != -1) {
             Funcionario f = new Funcionario();

            FuncionarioDAO dao = new FuncionarioDAO();

           
            f.setCod_Func((int)jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 0));

            dao.delete(f);
            
            txtNome.setText("");
            txtIdade.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Funcionário");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jtFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtFuncionariosMouseClicked

        if (jtFuncionarios.getSelectedRow() != -1) {

            txtID.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 0).toString());
            txtNome.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 1).toString());
            txtIdade.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 2).toString());
            txtRg.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 3).toString());
            txtCpf.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 4).toString());
            txtEndereco.setText(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 5).toString());
            cbCargo.setSelectedItem(jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 5).toString());

        }
    }//GEN-LAST:event_jtFuncionariosMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jtFuncionarios.getSelectedRow() != -1) {

            Funcionario f = new Funcionario();

            FuncionarioDAO dao = new FuncionarioDAO();

            f.setNome(txtNome.getText());
            f.setIdade(txtIdade.getText());
            f.setRg(txtRg.getText());
            f.setCpf(txtCpf.getText());
            f.setEndereco(txtEndereco.getText());
            f.setCargo((String) cbCargo.getSelectedItem());
            f.setCod_Func((int)jtFuncionarios.getValueAt(jtFuncionarios.getSelectedRow(), 0));

            dao.update(f);
            txtNome.setText("");
            txtIdade.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            readJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

