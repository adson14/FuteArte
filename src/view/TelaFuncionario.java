    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtFuncionarios.getModel();
        modelo.setNumRows(0);
       FuncionarioDAO fdao = new FuncionarioDAO();

        for (Funcionario f : fdao.read()) {
//            
            modelo.addRow(new Object[]{
                f.getCod_Func(),
                f.getNome(),
                f.getIdade(),
                f.getRg(),
                f.getCpf(),
                f.getEndereco(),
                f.getCargo()
            });
        }
    }

