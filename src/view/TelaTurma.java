    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtTurma.getModel();
        modelo.setNumRows(0);
        TurmaDAO tDAO = new TurmaDAO();

        for (Turma t : tDAO.listar()) {
//            
            modelo.addRow(new Object[]{
                t.getCod_Turma(),
                t.getNome(),
                t.getFUNCIONARIO_cod_Func()
            });
        }
    }

