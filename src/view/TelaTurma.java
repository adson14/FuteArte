    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Turma t = new Turma();
        TurmaDAO tDAO = new TurmaDAO();
        Funcionario funcionario = (Funcionario) cbFuncionario.getSelectedItem();

        t.setNome(txtNome.getText());
        t.setFUNCIONARIO_cod_Func((int) funcionario.getCod_Func());

        tDAO.create(t);
        txtNome.setText("");
        readJTable();


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jtTurma.getSelectedRow() != -1) {
            Turma t = new Turma();
            TurmaDAO tDAO = new TurmaDAO();
            
            t.setCod_Turma((int) jtTurma.getValueAt(jtTurma.getSelectedRow(), 0));
            tDAO.delete(t.getCod_Turma());

            txtNome.setText("");
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jtTurmaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtTurmaMouseClicked

        if (jtTurma.getSelectedRow() != -1) {

            txtID.setText(jtTurma.getValueAt(jtTurma.getSelectedRow(), 0).toString());
            txtNome.setText(jtTurma.getValueAt(jtTurma.getSelectedRow(), 1).toString());

        }
    }//GEN-LAST:event_jtTurmaMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jtTurma.getSelectedRow() != -1) {

            Turma t = new Turma();
            TurmaDAO tDAO = new TurmaDAO();
            Funcionario funcionario = (Funcionario) cbFuncionario.getSelectedItem();
            
            t.setCod_Turma((int) jtTurma.getValueAt(jtTurma.getSelectedRow(), 0));
            t.setNome(txtNome.getText());
            t.setFUNCIONARIO_cod_Func((int) funcionario.getCod_Func());

            tDAO.update(t);
            txtNome.setText("");
            readJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

