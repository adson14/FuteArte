    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Aluno a = new Aluno();
        AlunoDAO aDAO = new AlunoDAO();
        Funcionario funcionario = (Funcionario) cbFuncionario.getSelectedItem();
        Turma turma = (Turma) cbTurma.getSelectedItem();
        
        a.setNome(txtNome.getText());
        a.setIdade(Integer.parseInt(txtIdade.getText()));
        a.setRg(txtRg.getText());
        a.setCpf(txtCpf.getText());
        a.setEndereco(txtEndereco.getText());
        a.setStatusAvaliacaoMedica((String) cbStatus.getSelectedItem());
        a.setFUNCIONARIO_cod_Func((int) funcionario.getCod_Func());
        a.setTURMA_cod_Turma((int) turma.getCod_Turma());

        aDAO.create(a);
        txtNome.setText("");
        txtIdade.setText("");
        txtRg.setText("");
        txtCpf.setText("");
        txtEndereco.setText("");
        readJTable();


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        if (jtAlunos.getSelectedRow() != -1) {

            AlunoDAO aDAO = new AlunoDAO();
            MensalidadeDAO md = new MensalidadeDAO();
            
            aDAO.delete(Integer.parseInt(txtID.getText()));
            txtNome.setText("");
            txtIdade.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um Produto");
        }


    }//GEN-LAST:event_jButton2ActionPerformed

    private void jtAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAlunosMouseClicked

        if (jtAlunos.getSelectedRow() != -1) {

            txtID.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 0).toString());
            txtNome.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 1).toString());
            txtIdade.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 2).toString());
            txtRg.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 3).toString());
            txtCpf.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 4).toString());
            txtEndereco.setText(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 5).toString());
            cbStatus.setSelectedItem(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 6).toString());
            cbFuncionario.setSelectedItem(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 7).toString());
            cbTurma.setSelectedItem(jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 8).toString());

        }
    }//GEN-LAST:event_jtAlunosMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jtAlunos.getSelectedRow() != -1) {
            Aluno a = new Aluno();
            AlunoDAO aDAO = new AlunoDAO();
            Funcionario funcionario = (Funcionario) cbFuncionario.getSelectedItem();
            Turma turma = (Turma) cbTurma.getSelectedItem();

            a.setCod_Aluno((int) jtAlunos.getValueAt(jtAlunos.getSelectedRow(), 0));
            a.setNome(txtNome.getText());
            a.setIdade(Integer.parseInt(txtIdade.getText()));
            a.setRg(txtRg.getText());
            a.setCpf(txtCpf.getText());
            a.setEndereco(txtEndereco.getText());
            a.setStatusAvaliacaoMedica((String) cbStatus.getSelectedItem());
            a.setFUNCIONARIO_cod_Func((int) funcionario.getCod_Func());
            a.setTURMA_cod_Turma((int) turma.getCod_Turma());


            aDAO.update(a);
            txtNome.setText("");
            txtIdade.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
            readJTable();

        }
    }//GEN-LAST:event_jButton3ActionPerformed

