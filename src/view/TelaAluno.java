    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jtAlunos.getModel();
        modelo.setNumRows(0);
        AlunoDAO aDAO = new AlunoDAO();
        

        for (Aluno a : aDAO.listar()) {
//            
            modelo.addRow(new Object[]{
                a.getCod_Aluno(),
                a.getNome(),
                a.getIdade(),
                a.getRg(),
                a.getCpf(),
                a.getEndereco(),
                a.getStatusAvaliacaoMedica(),
                a.getFUNCIONARIO_cod_Func(),
                a.getTURMA_cod_Turma()
            });
        }
    }

